# + - * % // **
# int () int => int
# float () int => float
# int () float => float
# float () float => float
print(1 + 2)
print(1.1 + 2.9)

# /
# {int, float} () {int, float} => float
print(2 / 1)

# ------------------------------------------------------------------------------------------

# string: + *

# str + str => concatenate
print("Hello " + "World")

# str * int => duplicate
# int * str => duplicate
print("Title" * 2)

